COMMENT ON COLUMN MASTER.KLS_VETKA_OTP.MESTO_ID IS 'Место отгрузки';

COMMENT ON COLUMN MASTER.KLS_VETKA_OTP.LOAD_TYPE_ID IS 'Тип транспортировки';

COMMENT ON COLUMN MASTER.KLS_VETKA_OTP.OWNER_ID IS 'Владелец';

