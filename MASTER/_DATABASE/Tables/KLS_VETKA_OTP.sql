CREATE TABLE MASTER.KLS_VETKA_OTP
(
  ID            NUMBER(10)                      NOT NULL,
  VETKA_NAME    VARCHAR2(40 BYTE),
  SHORT_NAME    VARCHAR2(10 BYTE),
  DOSTUP        NUMBER(1),
  ORDNUNG       NUMBER(10),
  MESTO_ID      NUMBER(2),
  LOAD_TYPE_ID  NUMBER(2),
  OWNER_ID      NUMBER(6),
