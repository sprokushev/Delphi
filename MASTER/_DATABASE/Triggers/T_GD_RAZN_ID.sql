--
-- T_GD_RAZN_ID  (Trigger) 
--
CREATE OR REPLACE TRIGGER MASTER.T_GD_RAZN_ID
BEFORE INSERT
ON MASTER.GD_RAZN
FOR EACH ROW
DECLARE
tmpVar NUMBER;
BEGIN
   tmpVar := 0;

   IF :NEW.ID IS NULL OR :NEW.ID = 0
   THEN
      SELECT seq_gd_razn.NEXTVAL
        INTO tmpvar
        FROM DUAL;

      :NEW.ID := tmpvar;
   END IF;

END T_GD_RAZN_ID;
/


