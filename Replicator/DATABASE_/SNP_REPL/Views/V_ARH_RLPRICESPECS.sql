(RN, PRN, COMPANY, CRN, NOMNPACK, 
 NOMMODIF, NOMNMODIFPACK, GOODSPARTY, CURRENCY, PRICEMEAS, 
 PRICE, PRICEDATE, ENDDATE)
AS 
SELECT /*+ RULE */ "RN","PRN","COMPANY","CRN","NOMNPACK","NOMMODIF","NOMNMODIFPACK","GOODSPARTY","CURRENCY","PRICEMEAS","PRICE","PRICEDATE","ENDDATE" FROM PARUS.RLPRICESPECS
